    private void getCuaHangList(){
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        ArrayList<CuaHang_DTO> arr = new ArrayList<>();
        arr = chBUS.getAllCuaHang();
        for(int i =0;i<arr.size();i++){
            CuaHang_DTO ch = arr.get(i);
            String tench = ch.getTenCuaHang();
            String diachi= ch.getDiaChi();
            String tinhtrang = ch.getTinhTrang();
            Object[] row = {tench,diachi,tinhtrang};
            model.addRow(row);
        }
    }
    
