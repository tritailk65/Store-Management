    private void jTable1MousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MousePressed
        int i = jTable1.getSelectedRow();
        jTextField_TenCuaHang.setText(jTable1.getValueAt(i, 0)+"");
        jTextField_DiaChi.setText(jTable1.getValueAt(i, 1)+"");
        jTextField_TinhTrang.setText(jTable1.getValueAt(i, 2)+"");
        ArrayList<CuaHang_DTO> arr = new ArrayList<>();
        arr = chBUS.getAllCuaHang();
        for(int k=0;k<arr.size();k++){
            CuaHang_DTO ch = arr.get(k);
            if(ch.getTenCuaHang().equals(jTable1.getValueAt(i, 0)+"")){
                jTextField_MaCuaHang.setText(ch.getMaCH());
                jTextField_SoDienThoai.setText(ch.getSdt());
            }
        }
    }//GEN-LAST:event_jTable1MousePressed

