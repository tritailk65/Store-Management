    private void jButton_CTDT_San_PhamActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_CTDT_San_PhamActionPerformed
        jFrame_CTDT_TheoSanPham.setTitle("Chi tiết doanh thu theo sản phẩm");
        jFrame_CTDT_TheoSanPham.setVisible(true);
        
        DefaultTableModel model = (DefaultTableModel) jTable_DoanhThuTheoSp.getModel();
        model.setRowCount(0);
        
        jLabel_TenCH.setText(jComboBox_TenCuaHang.getSelectedItem().toString());
        jLabel_Thang_CT_theoSP.setText("Tháng "+(String) jComboBox_Thang.getSelectedItem());
        jLabel_Nam_CT_theoSP.setText("Năm "+(String) jComboBox_Nam.getSelectedItem());
        int thang = Integer.parseInt(jComboBox_Thang.getSelectedItem().toString());
        int nam = Integer.parseInt(jComboBox_Nam.getSelectedItem().toString());
        int tong = 0;
        ArrayList arr_tenSP = dt_BUS.getAll_tenSP();
        for(int i=0; i<arr_tenSP.size(); i++){
            int stt = i+1;
            String tenSP = (String) arr_tenSP.get(i);
            int sl_off = dt_BUS.tinh_soLuong_by_month_year_tenSP_idCH_moTa(thang, nam, tenSP, hd_BUS.find_id_cuaHang_By_TenCuaHang(jComboBox_TenCuaHang.getSelectedItem().toString()), "OFFLINE");
            int sl_on = dt_BUS.tinh_soLuong_by_month_year_tenSP_idCH_moTa(thang, nam, tenSP, hd_BUS.find_id_cuaHang_By_TenCuaHang(jComboBox_TenCuaHang.getSelectedItem().toString()), "ONLINE");
            int dt_off = dt_BUS.tinh_doanhThu_by_month_year_tenSP_idCH_moTa(thang, nam, tenSP, hd_BUS.find_id_cuaHang_By_TenCuaHang(jComboBox_TenCuaHang.getSelectedItem().toString()), "OFFLINE");
            int dt_on = dt_BUS.tinh_doanhThu_by_month_year_tenSP_idCH_moTa(thang, nam, tenSP, hd_BUS.find_id_cuaHang_By_TenCuaHang(jComboBox_TenCuaHang.getSelectedItem().toString()), "ONLINE");
            int tong_sl = sl_off + sl_on;
            int tong_dt = dt_off + dt_on;
            tong += tong_dt;
            Object[] row = {stt, tenSP, sl_off, dt_off, sl_on, dt_on, tong_sl, tong_dt};
            model.addRow(row);
        }
        jTextField_TongDT_CT_theoSP.setText(tong+"");
    }//GEN-LAST:event_jButton_CTDT_San_PhamActionPerformed

