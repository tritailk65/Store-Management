    private void jButton_KiemTra_DT_LapDoanhThuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_KiemTra_DT_LapDoanhThuActionPerformed
        // TODO add your handling code here:
        int thang = Integer.parseInt((String) jComboBox_Thang_LapDoanhThu.getSelectedItem());
        int nam = Integer.parseInt((String) jComboBox_Nam_LapDoanhThu.getSelectedItem());
        String id_CH = hd_BUS.find_id_cuaHang_By_TenCuaHang((String) jComboBox_CuaHang_LapDoanhThu.getSelectedItem());
        if(dt_BUS.is_any_DoanhThu_at_this_time(thang, nam, id_CH)){
            JOptionPane.showMessageDialog(this, "Thời gian này đã lập doanh thu");
        }else{
            if(dt_BUS.is_any_DoanhThu_has_changed_at_this_time(thang, nam, id_CH)){
                JOptionPane.showMessageDialog(this, "Thời gian này doanh thu có sự thay đổi cần lập doanh thu lại");
                signal_add_or_update_DoanhThu = false;
            }else{
                JOptionPane.showMessageDialog(this, "Thời gian này chưa lập doanh thu");
                signal_add_or_update_DoanhThu = true;
            }
            jButton_LapDoanhThu.setEnabled(true);
        }
    }//GEN-LAST:event_jButton_KiemTra_DT_LapDoanhThuActionPerformed

    private void jButton_LapDoanhThuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_LapDoanhThuActionPerformed
        // TODO add your handling code here:
        DoanhThu_DTO dt_dto = new DoanhThu_DTO();
        dt_dto.setId_cuaHang(hd_BUS.find_id_cuaHang_By_TenCuaHang((String) jComboBox_CuaHang_LapDoanhThu.getSelectedItem()));
        dt_dto.setId_NhanVien((String) jComboBox_idNhanVien_LapDoanhThu.getSelectedItem());
        dt_dto.setThang(Integer.parseInt((String) jComboBox_Thang_LapDoanhThu.getSelectedItem()));
        dt_dto.setNam(Integer.parseInt((String) jComboBox_Nam_LapDoanhThu.getSelectedItem()));
        if(signal_add_or_update_DoanhThu){
            if(dt_BUS.add_DoanhThu(dt_dto)){
                JOptionPane.showMessageDialog(this, "Lập doanh thu thành công");
                jButton_LapDoanhThu.setEnabled(false);
                reset_DoanhThuThangNay();
            }else{
                JOptionPane.showMessageDialog(this, "Lập doanh thu thất bại");
            }
        } else {
            // update doanh thu
            if(dt_BUS.update_DoanhThu(dt_dto)){
                JOptionPane.showMessageDialog(this, "Cập nhật doanh thu thành công");
                jButton_LapDoanhThu.setEnabled(false);
                reset_DoanhThuThangNay();
            }else{
                JOptionPane.showMessageDialog(this, "Cập nhật doanh thu thất bại");
            }
        }
        
    }//GEN-LAST:event_jButton_LapDoanhThuActionPerformed

