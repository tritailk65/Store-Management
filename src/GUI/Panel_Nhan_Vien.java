    private void getNhanVienList(){
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        ArrayList<NhanVien_DTO> arr = new ArrayList<>();
        arr = nvBUS.getAllNhanVien();
        for(int i =0;i<arr.size();i++){
            NhanVien_DTO nv = arr.get(i);
            String manv = nv.getMaNV();
            String hoten = nv.getHoTen();
            Object[] row = {i+1,manv,hoten};
            model.addRow(row);
        }
        ArrayList<CuaHang_DTO> arrCH = new ArrayList<>();
        arrCH=chBUS.getAllCuaHang();
        jComboBox_ThuocCuaHang.removeAllItems();
        for(int k =0;k<arrCH.size();k++){
            CuaHang_DTO ch = arrCH.get(k);
                jComboBox_ThuocCuaHang.addItem(ch.getTenCuaHang());
        }
        jComboBox_ThuocCuaHang.addItem(null);
        jComboBox_CTNV_ThuocCuaHang.removeAllItems();
        for(int j=0;j<jComboBox_ThuocCuaHang.getItemCount()-1;j++){
            jComboBox_CTNV_ThuocCuaHang.addItem(jComboBox_ThuocCuaHang.getItemAt(j));
        }
    }
    
