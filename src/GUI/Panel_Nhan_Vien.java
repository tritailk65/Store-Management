    private void jButton_ThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_ThemActionPerformed
        try{
            if(jTextField_MaNhanVien.getText().trim().equals("")||
                    jTextField_HoTenNhanVien.getText().trim().equals("")||
                    jTextField_ChucVu.getText().trim().equals("")||
                    jTextField_Sdt.getText().trim().equals("")||
                    jTextField_LuongCoBan.getText().trim().equals("")||
                    jTextArea_DanhGia.getText().trim().equals("")||
                    jTextField_SLDanhGia.getText().trim().equals("")
                    )
                JOptionPane.showMessageDialog(this, "Vui lòng nhập đầy đủ thông tin");
            else{
                NhanVien_DTO nvDTO=new NhanVien_DTO();
                nvDTO.setMaNV(jTextField_MaNhanVien.getText());
                ArrayList<CuaHang_DTO> arrCH = new ArrayList<>();
                arrCH=chBUS.getAllCuaHang();
                for(int h=0;h<arrCH.size();h++){
                    CuaHang_DTO ch = arrCH.get(h);
                    if(ch.getTenCuaHang().equals(jComboBox_ThuocCuaHang.getSelectedItem())){
                        nvDTO.setMaCH(ch.getMaCH());
                    }
                }
                nvDTO.setHoTen(jTextField_HoTenNhanVien.getText());
                nvDTO.setChucVu(jTextField_ChucVu.getText());
                nvDTO.setSdt(jTextField_Sdt.getText());
                nvDTO.setLuongCoBan(Double.parseDouble(jTextField_LuongCoBan.getText()));
                nvDTO.setDanhGiaCuaKhachHang(jTextArea_DanhGia.getText());
                nvDTO.setSoLuongDanhGia(Integer.parseInt(jTextField_SLDanhGia.getText()));
                JOptionPane.showMessageDialog(this,nvBUS.addCuaHang(nvDTO));
                jButton_LamMoiActionPerformed(evt);
            }
        }catch(NumberFormatException ex){
            JOptionPane.showMessageDialog(this,"Thông tin không hợp lệ");
        }
    }//GEN-LAST:event_jButton_ThemActionPerformed

